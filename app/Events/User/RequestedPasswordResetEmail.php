<?php

namespace Dsone\Events\User;

use Dsone\User;

class RequestedPasswordResetEmail
{
    /**
     * @var User
     */
    private $user;

    public function __construct(User $user)
    {
        $this->user = $user;
    }

    /**
     * @return User
     */
    public function getUser()
    {
        return $this->user;
    }
}
